const fs = require('fs');

//reading files asyncronosly
fs.readFile('js/Core_node_modules/fileSystem','utf-8',(err,files)=>{
    if(err){
        throw err;
    }
    else{
        console.log(files)
    }
});

// the following method will override the text already present inside FileSystem.text
// in order to avoid this we will use fs.appendFile method
fs.writeFile('js/Core_node_modules/fileSystem','My name is writeFile',(err)=>{
    if(err){
        throw err;
    }
    else{
        console.log("File is written.....")
    }
})

fs.appendFile('js/Core_node_modules/fileSystem',' this is islamia college',(err)=>{
    if(err){
        throw err
    }
    else{
        console.log("appending the data in file")
    }
})

// following method is used to rename the file 
fs.rename('js/Core_node_modules/fileSystem','js/Core_node_modules/simpleText',(err)=>{
    if(err){
        throw err
    }
    else{
        console.log("rename the file")
    }
})

// following method is used to delete the file 
fs.unlink('js/Core_node_modules/fileSystem',(err)=>{
    if(err){
        throw err
    }
    else{
        console.log("delete the file")
    }
})